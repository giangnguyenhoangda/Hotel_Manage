    private DefaultTableModel hienThiDanhSach(ArrayList<KhachHang> ds) {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Mã khách hàng");
        dtm.addColumn("Tên khách hàng");
        dtm.addColumn("Giới tính");
        dtm.addColumn("SĐT");
        dtm.addColumn("CMND");
        dtm.addColumn("Địa chỉ");
        for (KhachHang kh : ds) {
            Vector<String> v = new Vector<>();
            v.add(kh.getMaKhachHang());
            v.add(kh.getTenKhachHang());
            v.add(kh.getGioiTinh());
            v.add(kh.getSdt());
            v.add(kh.getCmnd());
            v.add(kh.getDiaChi());
            dtm.addRow(v);
        }
        return dtm;
    }

