    private void btnXoaNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaNhanVienActionPerformed
        if (tbl_nhanvien.getSelectedColumn() == -1) {
            return;
        }
        int ret = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa không ?", "Question ?", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ret == JOptionPane.YES_OPTION) {
            try {
                
                if (nhanVienService.xoaNhanVien(txtMaNhanVien.getText()) > 0) {
                    JOptionPane.showMessageDialog(null, "Xóa thành công !");
                    ds = nhanVienService.layDanhSachNhanVien();
                    loadData(ds);
                    xoaTrang();
                    
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnXoaNhanVienActionPerformed

