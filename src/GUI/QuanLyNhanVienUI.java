    private void btnThem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem1ActionPerformed
        NhanVien nv = new NhanVien();
        nv.setMaNhanVien(txtMaNhanVien.getText());
        nv.setTenNhanVien(txtTen.getText());
        nv.setChucVu(dsChucVu.get(cbChucVu.getSelectedIndex()));
        nv.setGioiTinh(cbGioiTinh.getSelectedItem() + "");
        nv.setcMND(txtcmnd.getText());
        nv.setThuong(Integer.parseInt(txttienthuong.getText()));
        nv.setDiaChi(txtDiaChi.getText());
        nv.setsDT(txtSDT.getText());
        
        if (nhanVienService.themNhanVien(nv) != -1) {
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            xoaTrang();
            ds = nhanVienService.layDanhSachNhanVien();
            loadData(ds);
        } else {
            JOptionPane.showMessageDialog(null, "Thêm thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
        };
        

    }//GEN-LAST:event_btnThem1ActionPerformed

