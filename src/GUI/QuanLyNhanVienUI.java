    private void btnSuaNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaNhanVienActionPerformed
        
        if (nhanVienService.suaNhanVien(new NhanVien(txtMaNhanVien.getText(), txtTen.getText(), cbGioiTinh.getSelectedItem() + "", txtcmnd.getText(), txtSDT.getText(), txtDiaChi.getText(), Integer.parseInt(txttienthuong.getText()), dsChucVu.get(cbChucVu.getSelectedIndex()))) != -1) {
            JOptionPane.showMessageDialog(null, "sửa thành công");
            ds = nhanVienService.layDanhSachNhanVien();
            loadData(ds);
            xoaTrang();
        };
        

    }//GEN-LAST:event_btnSuaNhanVienActionPerformed
    private void xoaTrang() {
        txtMaNhanVien.setText("");
        txtTen.setText("");
        
        txtDiaChi.setText("");
        txttienthuong.setText("");
        txtcmnd.setText("");
        txtSDT.setText("");
        cbGioiTinh.setSelectedIndex(0);
    }
    
