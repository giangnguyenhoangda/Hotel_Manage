    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int[] rowChon = jTable2.getSelectedRows();
        if (rowChon.length <= 0) {
            JOptionPane.showMessageDialog(null, "Hãy chọn dịch vụ muốn thêm.");
        } else {
            for (int i : rowChon) {
                DichVu dv = new DichVu();
                ChiTietDichVu ct = new ChiTietDichVu();
                String maDichVu = jTable2.getValueAt(i, 0) + "";
                dv.setMaDichVu(maDichVu);
                ct.setMaDichVu(maDichVu);
                try {
                    dv.setTenDichVu(jTable2.getValueAt(i, 1) + "");
                    String strGia = jTable2.getValueAt(i, 2) + "";
                    Integer gia = Integer.parseInt(strGia.substring(0, strGia.length() - 4));
                    dv.setGiaDichVu(gia);
                } catch (Exception e) {
                    e.printStackTrace();
                }
                ct.setMaDatPhong(maDatPhong);
                ct.setMaHoaDon(maHoaDon);
                ct.setMaPhong(maPhong);
                if (dsChiTietDichVuTamThoi.contains(ct)) {
                    for (ChiTietDichVu chiTiet : dsChiTietDichVuTamThoi) {
                        if (ct.equals(chiTiet)) {
                            int soLuong = chiTiet.getSoLuong();
//                            soLuong++;
                            try {
                                int soLuongThem = 1;
                                if (txtSoLuong.getText().matches("\\d+")) {
                                    soLuongThem = Integer.parseInt(txtSoLuong.getText());
                                }
                                soLuong += soLuongThem;
                            } catch (Exception e) {

                            }
                            chiTiet.setSoLuong(soLuong);
                            break;
                        }
                    }
                } else {
                    ct.setSoLuong(1);
                    dsDichVuTamThoi.add(dv);
                    dsChiTietDichVuTamThoi.add(ct);
                }

            }
            hienThiThongTinDichVuCuaPhong(dsChiTietDichVuTamThoi, dsDichVuTamThoi);
        }

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int[] rowChon = jTable1.getSelectedRows();
        if (rowChon.length <= 0) {
            JOptionPane.showMessageDialog(null, "Hãy chọn dịch vụ từ giỏ muốn xóa");
        } else {
            for (int i = rowChon.length - 1; i >= 0; i--) {
                int viTri = rowChon[i];
                ChiTietDichVu ct = dsChiTietDichVuTamThoi.get(rowChon[i]);
                DichVu dv = dsDichVuTamThoi.get(rowChon[i]);
                try {
                    String strSoLuong = jTable1.getValueAt(rowChon[i], 2) + "";
                    Integer soLuong = Integer.parseInt(strSoLuong);
                    System.out.println("Số lượng: " + soLuong);
                    if (dsChiTietDichVuDung.contains(ct)) {
                        int soLuongDaDung = 0;
                        for (ChiTietDichVu c : dsChiTietDichVuDung) {
                            if (c.equals(ct)) {
                                soLuongDaDung = c.getSoLuong();
                                break;
                            }
                        }
                        try {
                            int soLuongXoa = 1;
                            if (txtSoLuong.getText().matches("\\d+")) {
                                soLuongXoa = Integer.parseInt(txtSoLuong.getText());
                            }
                            soLuong -= soLuongXoa;
                            if (soLuong < soLuongDaDung) {
                                JOptionPane.showMessageDialog(null, "Phòng " + maPhong + " đã đăng ký sử dụng " + soLuongDaDung + " " + dv.getTenDichVu() + " trước đó.");
                                return;
                            } else {
//                                soLuong--;
                                ct.setSoLuong(soLuong);
                            }
                        } catch (Exception e) {
                            e.printStackTrace();
                        }
                    } else {
                        if (soLuong == 1) {
                            System.out.println("Xóa" + dv.getTenDichVu());
                            dsChiTietDichVuTamThoi.remove(ct);
                            dsDichVuTamThoi.remove(dv);
                        } else {
                            System.out.println(dv.getTenDichVu() + "Trừ 1");
                            soLuong--;
                            ct.setSoLuong(soLuong);
                        }
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            hienThiThongTinDichVuCuaPhong(dsChiTietDichVuTamThoi, dsDichVuTamThoi);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

