    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String maKhachHang = txtMaKhachHangQL.getText();
        String tenKhachHang = txtTenKhachHangQL.getText();
        String sdt = txtPhoneQL.getText();
        String cmnd = txtCMNDQL.getText();
        String diaChi = txtDiaChiQL.getText();
        String gioiTinh = "";
        if (cbxGioiTinhQL.getSelectedIndex() != -1) {
            gioiTinh = cbxGioiTinhQL.getSelectedItem().toString();
        }
        if (maKhachHang.equals("") && tenKhachHang.equals("") && cbxGioiTinhQL.getSelectedIndex() == -1 && sdt.equals("") && cmnd.equals("") && diaChi.equals("")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập dữ liệu");
            return;
        } else if (!sdt.matches("\\d+|\\s*")) {
            JOptionPane.showMessageDialog(null, "Nhập sai số điện thoại");
            return;
        } else if (!sdt.matches("\\d{12}|\\s*")) {
            JOptionPane.showMessageDialog(null, "Nhập sai số điện thoại");
            return;
        } else {
            ArrayList<KhachHang> ds = null;
            try {
                ds = bllKhachHang.timKiemKhachHang(maKhachHang, tenKhachHang, gioiTinh, sdt, cmnd, diaChi);
            } catch (Exception ex) {
                ex.getStackTrace();
            }
            dtmTimKiemKhachHang = hienThiDanhSach(ds);
            tblQuanLyKhachHang.setModel(dtmTimKiemKhachHang);
            btnLast = btnTimKiem;
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnThemKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemKhachHangActionPerformed
        // TODO add your handling code here:
        String maKhachHang = txtMaKhachHangQL.getText();
        String tenKhachHang = txtTenKhachHangQL.getText();
        String sdt = txtPhoneQL.getText();
        String cmnd = txtCMNDQL.getText();
        String diaChi = txtDiaChiQL.getText();
        String gioiTinh = "";
        if (cbxGioiTinhQL.getSelectedIndex() != -1) {
            gioiTinh = cbxGioiTinhQL.getSelectedItem().toString();
        }
        String kiemTra = KiemTraDuLieuNhapThoaMan.kiemTraDuLieuNhapCapNhatKhachHang(tenKhachHang, gioiTinh, sdt, cmnd, diaChi);
        if (kiemTra.equals("Chưa nhập dữ liệu")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập dữ liệu");
            return;
        }
        if (kiemTra.equals("Chưa nhập tên khách hàng")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập tên khách hàng");
            return;
        }
        if (kiemTra.equals("Chưa chọn giới tính")) {
            JOptionPane.showMessageDialog(null, "Chưa chọn giới tính");
            return;
        }
        if (kiemTra.equals("Chưa nhập sđt")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập sđt");
            return;
        }
        if (kiemTra.equals(sdt + " không phải số điện thoại")) {
            JOptionPane.showMessageDialog(null, "Nhập sai sđt");
            return;
        }
        if (kiemTra.equals("Chưa nhập CMND")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập CMND");
            return;
        }
        if (kiemTra.equals("CMND phải có 9 hoặc 12 số")) {
            JOptionPane.showMessageDialog(null, "CMND phải có 9 hoặc 12 số");
            return;
        }
        if (kiemTra.equals("Chưa nhập địa chỉ")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập địa chỉ");
            return;
        }
        if (kiemTra.equals("Dữ liệu thỏa mãn")) {
            int kt = JOptionPane.showConfirmDialog(null, "Bạn có muốn thêm không ?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (kt == JOptionPane.YES_OPTION) {
                int kq = 0;
                try {
                    kq = bllKhachHang.themKhachHang(maKhachHang, tenKhachHang, gioiTinh, sdt, cmnd, diaChi);
                    System.out.println(kq);
                    if (kq > 0) {
                        JOptionPane.showMessageDialog(null, "Thêm thành công");
                        hienThiDanhSachKhachHang();
                        tblQuanLyKhachHang.setModel(dtmQuanLyKhachHang);
                    } else {
                        JOptionPane.showMessageDialog(null, "Thêm thất bại");
                    }
                    resetCapNhat();
                } catch (Exception ex) {
                    ex.getStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btnThemKhachHangActionPerformed

