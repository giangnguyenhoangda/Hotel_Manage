    private void loadData(ArrayList<NhanVien> ds1) {
        //To change body of generated methods, choose Tools | Templates.

        dtm = (DefaultTableModel) tbl_nhanvien.getModel();
        dtm.setRowCount(0);
        for (NhanVien nv : ds1) {
            Vector<Object> vt = new Vector<>();
            vt.add(nv.getMaNhanVien());
            vt.add(nv.getTenNhanVien());
            vt.add(nv.getGioiTinh());
            vt.add(nv.getDiaChi());
            vt.add(nv.getsDT());
            vt.add(nv.getcMND());
            vt.add(nv.getThuong());
            vt.add(nv.getChucVu().getTenChucVu());
            dtm.addRow(vt);
            
        }
        
    }
