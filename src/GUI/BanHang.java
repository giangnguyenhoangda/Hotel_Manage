    private ArrayList<DichVu> layDanhSachDichVuDaDung(ArrayList<ChiTietDichVu> dsChiTiet) {
        ArrayList<DichVu> dsDichVu = new ArrayList<>();
        for (ChiTietDichVu ct : dsChiTiet) {
            DichVu dv = bllDichVu.timKiemDichVu(ct.getMaDichVu());
            dsDichVu.add(dv);
        }
        return dsDichVu;
    }

    private void hienThiThongTinDichVuCuaPhong(ArrayList<ChiTietDichVu> dsChiTiet, ArrayList<DichVu> dsDichVu) {
        tongTien = 0;
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Mã dịch vụ");
        dtm.addColumn("Tên dịch vụ");
        dtm.addColumn("Số lượng");
        dtm.addColumn("Đơn giá");
        dtm.addColumn("Thành tiền");
        for (int i = 0; i < dsChiTiet.size(); i++) {
            ChiTietDichVu ct = dsChiTiet.get(i);
            DichVu dv = dsDichVu.get(i);
            Vector<String> v = new Vector<>();
            v.add(dv.getMaDichVu());
            v.add(dv.getTenDichVu());
            Integer soLuong = ct.getSoLuong();
            v.add(soLuong + "");
            Integer donGia = dv.getGiaDichVu();
            v.add(donGia + " VND");
            Integer thanhTien = donGia * soLuong;
            tongTien += thanhTien;
            v.add(thanhTien + " VND");
            dtm.addRow(v);
            System.out.println("Mã dịch vụ: " + ct.getMaDichVu() + " " + ct.getMaDatPhong() + " " + ct.getMaHoaDon() + " " + ct.getMaPhong() + " " + ct.getSoLuong());
        }
        jTable1.setModel(dtm);
        hienThiTien();
    }

    private void hienThiDichVuLenBangDichVuTuArrayList(ArrayList<DichVu> ds) {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Mã dịch vụ");
        dtm.addColumn("Tên dịch vụ");
        dtm.addColumn("Đơn giá");
        for (DichVu dv : ds) {
            Vector<String> v = new Vector<>();
            v.add(dv.getMaDichVu());
            v.add(dv.getTenDichVu());
            v.add(dv.getGiaDichVu() + " VND");
            dtm.addRow(v);
        }
        jTable2.setModel(dtm);
    }

